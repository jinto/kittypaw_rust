//! Integration tests for the agent loop and event processing pipeline.
//!
//! These tests use a deterministic mock LLM provider to verify the full
//! Event → LLM → Sandbox → SkillExecutor → Response chain without real API calls.

use std::sync::Arc;
use tokio::sync::Mutex;

use async_trait::async_trait;
use kittypaw_core::config::Config;
use kittypaw_core::error::Result;
use kittypaw_core::types::{Event, EventType, LlmMessage};
use kittypaw_llm::provider::{LlmProvider, LlmResponse};
use kittypaw_sandbox::Sandbox;
use kittypaw_store::Store;

// ── Mock LLM Provider ──────────────────────────────────────────────────

/// A mock provider that returns pre-determined JS code as if generated by an LLM.
struct MockJsProvider {
    js_code: String,
}

impl MockJsProvider {
    fn new(js: &str) -> Self {
        Self {
            js_code: js.to_string(),
        }
    }
}

#[async_trait]
impl LlmProvider for MockJsProvider {
    async fn generate(&self, _messages: &[LlmMessage]) -> Result<LlmResponse> {
        Ok(LlmResponse::text_only(self.js_code.clone()))
    }
}

// ── Helpers ─────────────────────────────────────────────────────────────

fn test_config() -> Config {
    let mut config = Config::default();
    config.freeform_fallback = true; // allow natural language → agent_loop
    config
}

fn telegram_event(text: &str, chat_id: &str) -> Event {
    Event {
        event_type: EventType::Telegram,
        payload: serde_json::json!({
            "text": text,
            "chat_id": chat_id,
            "from_name": "test_user",
        }),
    }
}

fn desktop_event(text: &str) -> Event {
    Event {
        event_type: EventType::Desktop,
        payload: serde_json::json!({
            "text": text,
            "workspace_id": "test",
        }),
    }
}

// ── Tests ───────────────────────────────────────────────────────────────

/// Agent loop processes a simple return statement and produces output.
#[tokio::test]
async fn test_agent_loop_simple_return() {
    let provider = MockJsProvider::new(r#"return "hello from agent";"#);
    let config = test_config();
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    let store = Arc::new(Mutex::new(
        Store::open(":memory:").expect("in-memory store"),
    ));

    let session = kittypaw_cli::agent_loop::AgentSession {
        provider: &provider,
        fallback_provider: None,
        sandbox: &sandbox,
        store: store.clone(),
        config: &config,
        on_token: None,
        on_permission_request: None,
    };

    let event = desktop_event("say hello");
    let result = session.run(event).await;
    assert!(result.is_ok(), "agent_loop should succeed: {:?}", result);
    assert_eq!(result.unwrap(), "hello from agent");
}

/// Agent loop handles Storage.get/set skill calls within sandbox execution.
#[tokio::test]
async fn test_agent_loop_storage_round_trip() {
    let provider = MockJsProvider::new(
        r#"
        await Storage.set("greeting", "world");
        const val = await Storage.get("greeting");
        return "stored: " + val;
        "#,
    );
    let config = test_config();
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    let store = Arc::new(Mutex::new(
        Store::open(":memory:").expect("in-memory store"),
    ));

    let session = kittypaw_cli::agent_loop::AgentSession {
        provider: &provider,
        fallback_provider: None,
        sandbox: &sandbox,
        store: store.clone(),
        config: &config,
        on_token: None,
        on_permission_request: None,
    };

    let result = session.run(desktop_event("store something")).await;
    assert!(result.is_ok(), "storage round-trip: {:?}", result);
    let text = result.unwrap();
    // Storage.get returns JSON {"value":"world"}, so the concatenation includes it
    assert!(
        text.contains("world"),
        "expected stored value containing 'world', got: {text}"
    );
}

/// Agent loop creates a session/agent state that persists across calls.
#[tokio::test]
async fn test_agent_session_state_persists() {
    let provider = MockJsProvider::new(r#"return "ok";"#);
    let config = test_config();
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    let store = Arc::new(Mutex::new(
        Store::open(":memory:").expect("in-memory store"),
    ));

    let session = kittypaw_cli::agent_loop::AgentSession {
        provider: &provider,
        fallback_provider: None,
        sandbox: &sandbox,
        store: store.clone(),
        config: &config,
        on_token: None,
        on_permission_request: None,
    };

    // First call creates state
    let r1 = session.run(desktop_event("first")).await;
    assert!(r1.is_ok());

    // Second call reuses state — agent should have 2 user turns + 2 assistant turns
    let r2 = session.run(desktop_event("second")).await;
    assert!(r2.is_ok());

    // Verify state was persisted
    let s = store.lock().await;
    let state = s
        .load_state("desktop-test")
        .expect("load_state")
        .expect("state should exist");
    // 2 user turns + 2 assistant turns = 4 total
    assert!(
        state.turns.len() >= 4,
        "expected >= 4 turns, got {}",
        state.turns.len()
    );
}

