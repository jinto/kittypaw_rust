/// Bootstrap parity: Config::default() produces a valid config that
/// AgentSession can use without panicking.
#[tokio::test]
async fn test_bootstrap_parity_default_config() {
    let config = Config::default();

    // Verify essential fields have sane defaults
    assert!(config.sandbox.timeout_secs > 0);
    assert!(config.sandbox.memory_limit_mb > 0);
    assert!(config.sandbox.allowed_paths.is_empty()); // backward compat: no restrictions
    assert!(config.agents.is_empty());
    assert!(config.channels.is_empty());

    // Verify Store can be opened with in-memory path
    let store = Store::open(":memory:");
    assert!(store.is_ok(), "in-memory store should open");

    // Verify Sandbox can be created from config
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    // Simple execution should work without panicking
    let result = sandbox.execute("return 42;", serde_json::json!({})).await;
    assert!(result.is_ok(), "sandbox should execute: {:?}", result);
}

/// When freeform_fallback is false, non-slash messages without a matching
/// skill trigger return a helpful message instead of invoking the LLM.
